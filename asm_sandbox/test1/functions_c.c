#include "functions_c.h"

int32_t add_numbers_32_c(const int32_t a, const int32_t b)
{
    return a + b;
}

int32_t mul_numbers_32_c(const int32_t a, const int32_t b)
{
    return a * b;
}

int64_t add_numbers_64_c(const int64_t a, const int64_t b)
{
    return a + b;
}

int64_t mul_numbers_64_c(const int64_t a, const int64_t b)
{
    return a * b;
}
